// Com o typeof é possível ver o tipo de dado no javascript

let nome = "Amanda"; // string -> sequência de chars
let idade = 20;
let altura = 1.66;
let genero = "f";
let atleta = true;
let notas = [6, 5, 7, 6];
let soma = (a, b) => a + b;
let skill = { forca: 10, velocidade: 20 };

typeof nome; // string
typeof idade; // number
typeof altura; // number
typeof genero; // string
typeof atleta; // boolean
typeof notas; // object -> na verdade é um Array
typeof soma; // function
typeof skill; // object
