// jun칞칚o de arrays

let frutas = ["游본", "游꼔", "游볳"];
let animais = ["游냤", "游냠", "游냜"];

// junta as listas formando uma s칩
frutas.contat(animais); //-> ['游본', '游꼔', '游볳', '游냤', '游냠', '游냜']

// junta as listas formando uma s칩
[(frutas, animais)].flat(); //-> ['游본', '游꼔', '游볳', '游냤', '游냠', '游냜']

// junta as frutas em uma string separado por pra칞o um tra칞o
frutas.join("-"); //-> "游본-游꼔-游볳-游꼡-游-游꼖";
